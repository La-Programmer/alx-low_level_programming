/**
* List of function prototypes
*/

/**
* _putchar - returns a character
* @c: Character to be printed
*
* Return: Character
*/
int _putchar(char c);

/**
* print_alphabet - prints the lowercase alphabet
*
* Return: nothing
*/
void print_alphabet(void);

/**
* print_alphabet_x10 - prints the lowercase alphabet 10 times
*
* Return: nothing
*/
void print_alphabet_x10(void);

/**
* _islower - checks if a character is lowercase
* @c: Any random character
*
* Return: 1 if true, 0 if false
*/
int _islower(int c);

/**
* _isalpha - checks if a character is an alphabet
* @c: Character to be checked
*
* Return: 1 if true, 0 if false
*/
int _isalpha(int c);

/**
* print_sign - prints the sign of a number
* @n: Number to be checked
*
* Return: 1 if positive, 0 if zero and -1 if negative
*/
int print_sign(int n);

/**
* _abs - Computes the absolute value of an integer
* @: Number to be evaluated
*
* Return: Returns the absolute value of the inputted integer 
*/
int _abs(int);

/**
* print_last_digit - Prints the last digit of a number
* @: Number to be evaluated
*
* Return: Returns the last digit of the input
*/
int print_last_digit(int);

/**
* jack_bauer - prints every minute of the day from 00:00 to 23:59
*
* Return: nothing
*/
void jack_bauer(void);

/**
* times_table - Prints the 9 times table starting from 0
*
* Return: nothing
*/
void times_table(void);

/**
* add - Adds two integers
* @int, int: Integers to be added
*
* Return: Returns sum of the two inputs
*/
int add (int, int);

/**
* print_to_98
* @n: Number to start from
*
* Return: nothing
*/
void print_to_98(int n);
